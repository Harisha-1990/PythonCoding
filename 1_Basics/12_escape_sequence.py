#escape sequence
#escape sequence is a backslash \ that lets python know whatever comes after this is a string
weather = 'It\'s kind of sunny'
print(weather)
weather = 'It\'s "kind of" sunny.'
print(weather)
# if we want to have a actual backslash

weather = 'It\'\s kind of funny'
print(weather)
#\t
# it indicates whatever comes after backslash has a tab
weather = 'It\t\'s kind of sunny'
print(weather)
#\n 
# it indicates watever comes after backslash has a new line
weather = 'It\'s kind of sunny \nhope you have a good day'
print(weather)
